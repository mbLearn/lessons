## R language Explained ##
#1. The “<-” tells R to take the number to the right of the symbol 
#   and store it in a variable whose name is given on the left. 

a <- 3
a

#2. Some mathematical operations

b <- sqrt(a*a+3)
b

#3. If you want to get a list of the variables that you have defined 
#   in a particular session you can list them all using the ls command:
ls()

#4. You are not limited to just saving a single number. 
#   You can create a list (also called a “vector”) using the c command:
d <- c(1,2,3,4)
d

